
public class SwitchCase {
	
	public void switchCase() {
		int i = 1;
		switch(i) {
			case 1:{
				System.out.println(i);
				break;
			}
			case 2:
				System.out.println(i);
				System.out.println(i);
				System.out.println(i);
				break;
			default:
				System.out.println(i);
			
		    
		}
	}

}
